// Función para LEER del LocalStorage
export const getHabitaciones = () => {
  const guardadas = localStorage.getItem('hotel_habitaciones');
  if (guardadas) {
    return JSON.parse(guardadas);
  } else {
    // Si no hay nada guardado (primera vez), guardamos las iniciales
    localStorage.setItem('hotel_habitaciones', JSON.stringify(habitacionesIniciales));
    return habitacionesIniciales;
  }
};

// Función para GUARDAR en el LocalStorage
export const saveHabitaciones = (habitaciones) => {
  localStorage.setItem('hotel_habitaciones', JSON.stringify(habitaciones));
};